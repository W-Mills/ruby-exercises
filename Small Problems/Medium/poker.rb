class Card
  include Comparable

  attr_reader :rank, :suit

  ALL_VALUES = (1..14).to_a

  VALUE = { "Jack" => 11,
            "Queen" => 12,
            "King" => 13,
            "Ace" => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def value
    if rank.is_a?(Integer)
      value = rank
    else
      value = VALUE[rank]
    end
    value
  end

  def <=>(other)
    value <=> other.value
  end

  def to_s
    "#{rank} of #{suit}"
  end
end

class Deck
  attr_accessor :deck

  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    fresh_deck
  end

  def draw
    fresh_deck if deck.empty?
    deck.pop
  end

  def fresh_deck
    self.deck = []
    SUITS.each do |suit|
      RANKS.each do |rank|
        deck << Card.new(rank, suit)
      end
    end
    deck.shuffle!
  end
end

class PokerHand
  attr_accessor :deck, :rank_count
  attr_reader :hand

  def initialize(deck)
    @deck = deck
    @hand = []
    @rank_count = Hash.new(0)
    5.times do
      card = deck.draw
      hand << card
      rank_count[card.value] += 1
    end
  end

  def print
    puts hand
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  def to_s
    hand.each do |card|
      card.to_s
    end
  end

  private

  def n_of_a_kind?(number)
    rank_count.one?{ |_, count| count == number }
  end

  def royal_flush?
    straight_flush? && hand.min.value == 10
  end

  def straight_flush?
    flush? && straight?
  end

  def four_of_a_kind?
    n_of_a_kind?(4)
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def flush?
    suits_in_hand = hand.map {|card| card.suit}
    suits_in_hand.uniq.size == 1
  end

  def straight?
    return false if rank_count.any? { |_, count| count > 1 }
    hand.min.value == hand.max.value - 4
  end

  def three_of_a_kind?
    n_of_a_kind?(3)
  end

  def two_pair?
    # values = hand.map { |card| card.value }
    # values.uniq.size == 3
    rank_count.select { |_, count| count == 2 }.size == 2
  end

  def pair?
    n_of_a_kind?(2)
  end
end

